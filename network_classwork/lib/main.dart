@JsonSerializable()
class Album {
  final int userId;
  final int id;
  final String title;

  const Album({
    required this.userId,
    required this.id,
    required this.title,
  });

  Map<String, dynamic> _$AlbumToJson(Album instance) => <String, dynamic>{
        'userId': instance.userId,
        'title': instance.title,
      };

  // factory Album.fromJson(Map<String, dynamic> json) {
  //   return Album(
